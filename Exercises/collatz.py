# The Collatz conjecture program

n = 24       

while n != 1:
    if  n % 2 == 0:
        n = n / 2
        print(n)                      
    elif n % 2 == 1: 
        n = n * 3 + 1                     
        print(n)                      
    else:
        print(n)




   











    







 


    
